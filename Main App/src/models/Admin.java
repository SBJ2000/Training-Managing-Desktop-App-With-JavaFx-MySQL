package models;

public class Admin {
	 	private int id;

	    private String name;

	    private String password;

	    public int getid ()
	    {
	        return id;
	    }

	    public void setId (int id)
	    {
	        this.id = id;
	    }

	    public String getname ()
	    {
	        return name;
	    }

	    public void setname (String name)
	    {
	        this.name = name;
	    }
	    public String getpassword ()
	    {
	        return password;
	    }

	    public void setpassword (String password)
	    {
	        this.password = password;
	    }
}
